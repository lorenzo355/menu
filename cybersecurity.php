    <h3>LA DIRETTIVA EUROPEA NIS2 COINVOLGE ANCHE LE PMI</h3>
    <p>La direttiva NIS2, abbreviazione di “Directive on Security of Network and Information Systems”, è una legislazione dell’Unione Europea (UE) che mira a migliorare la sicurezza delle reti e dei sistemi informativi nell’UE. È stata adottata per affrontare le minacce emergenti nel cyberspazio e per garantire un livello elevato e coerente di sicurezza delle reti e dei sistemi informativi in tutti gli Stati membri.</p>
    <p>Le PMI, che di fatto erano rimaste ben al di fuori della portata della NIS , Potrebbero dover rispondere in concreto nel caso in cui si verificassero delle violazioni dei dati e dei sistemi in cui hanno voce in capitolo per via di un contratto di fornitura.</p>
    <p>In altri termini, nella malaugurata ipotesi in cui dovesse verificarsi un incidente di sicurezza informatica, a risponderne non sarà più soltanto l’azienda titolare del servizio, ma anche gli altri stakeholder che intervengono lungo la supply chain.</p>
    <p><strong>La direttiva, già recepita in diversi Paesi UE, In Italia dovrà esserlo entro Ottobre 2024.</strong></p>
